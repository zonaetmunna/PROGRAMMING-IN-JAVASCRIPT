/* condition handle with switch */

let day = "Monday";

switch (day) {
  case "Monday":
    console.log("It's the start of the week.");
    break;
  case "Tuesday":
    console.log("It's the second day of the week.");
    break;
  case "Wednesday":
    console.log("It's the middle of the week.");
    break;
  default:
    console.log("It's another day of the week.");
}
